    private void registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroActionPerformed
        
        
        
        Date checkIn = fechaCheckIn.getDate();
        Date checkOut = fechaCheckOut.getDate();
        String tipoHabitacion = (String) habitacion.getSelectedItem();
        String comoPagar = (String) formaPago.getSelectedItem();
        double pago = costoTotal;
        int numReserva = generarNumero.generateRandomNumber();
        
        guardarReserva.Guardar(checkIn, checkOut, tipoHabitacion, comoPagar, pago,
                numReserva, fechaCheckIn, fechaCheckOut);
        
       this.setVisible(false);
        
    }//GEN-LAST:event_registroActionPerformed


    double costoTotal;
    PropertyChangeListener dateChangeListener  = evt -> {
            Date checkIn = fechaCheckIn.getDate();
            Date checkOut = fechaCheckOut.getDate();
            String seleccion = (String) habitacion.getSelectedItem();
            
            if(checkIn != null && checkOut != null){

                costoTotal = precioReserva.calcularCosto(checkIn, checkOut, seleccion);
                precioTotal.setText(costoTotal + "$ dólares");
            } 
    };

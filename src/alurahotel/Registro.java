    private void guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMouseClicked
       
        String numeroId = id.getText();
        
        String getNombre = nombre.getText();
        String getApellido = apellido.getText();
        Date getFecha = fechaNacimiento.getDate();
        String getTelefono = telefono.getText();
        
        String getReserva = numReserva.getText();
        int reserva = Integer.parseInt(getReserva);
        
        guardarRegistro.guardarRegistro(numeroId, getNombre, getApellido, getFecha,
                getTelefono, reserva, this, nombre, apellido,
                apellido, numReserva, telefono, fechaNacimiento);
        
    }//GEN-LAST:event_guardarMouseClicked


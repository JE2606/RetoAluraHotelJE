    private void nombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nombreKeyTyped
        char valido = evt.getKeyChar();
        if(Character.isDigit(valido)){
            getToolkit().beep();
            evt.consume();
            List<String> botones = Arrays.asList("Entendido");
                PanelesPersonalizados optionPane = new PanelesPersonalizados(
                    "Por favor, solo ingrese letras.",
                    "Tipo de dato incompatible",
                botones,
                  "/alurahotel/imagenes/incompatible.png",
                 60,
                 60
                );
                optionPane.Mostrar();
        }
            
    }//GEN-LAST:event_nombreKeyTyped

